/*
英文字串裡第一個字母在陣列裡是第0組
假如第一個字母是大寫 , 就回傳大寫
假如第一個字母是小寫 , 也回傳大寫

*/


function capitalize(str) {

    var firstAlphabet = str[0]
    if (firstAlphabet >= 'A' && firstAlphabet <= 'Z') 
    return (firstAlphabet >= 'A' && firstAlphabet <= 'Z')
    else return (firstAlphabet >= 'A' && firstAlphabet <= 'Z')

}

capitalize('nick')


